#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	m;
	int	num;

	i = 0;
	m = 1;
	num = 0;
	while ((str[i] > 8 && str[i] < 14) || str[i] == ' ')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			m = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		num = (num * 10) + (str[i] - 48);
		i++;
	}
	return (num * m);
}
