#include "libft.h"

void	*ft_memset(void *ptr, int c, size_t len)
{
	size_t	i;

	i = 0;
	while (i < len)
	{
		*(unsigned char *)(ptr + i) = (unsigned char)c;
		i++;
	}
	return (ptr);
}
