#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	size_t	i;
	int		*ar;

	i = 0;
	ar = (int *)malloc(count * size);
	if (ar)
	{
		while (i < count)
		{
			ar[i] = 0;
			i++;
		}
		return (ar);
	}
	return (NULL);
}
